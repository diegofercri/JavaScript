// var (no usar, problemas de definicion)
var helloWorld = "¡Hola Mundo!"
console.log(helloWorld)

hellowWorld ="¡Hola Diego!"
console.log(hellowWorld)

// let (solo son accesibles dentro del bloque)
let hellowWorld2 ="¡Hola Mundo!"
console.log(hellowWorld2)

hellowWorld2 ="¡Hola Diego!"
console.log(hellowWorld2)

// const (son como let, pero son constantes, es decir, su valor inicial es invariable)
const hellowWorld3 ="¡Hola Mundo!"
console.log(hellowWorld3)

hellowWorld3 ="¡Hola Diego!"
console.log(hellowWorld3)